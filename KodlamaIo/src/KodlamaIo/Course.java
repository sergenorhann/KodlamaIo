package KodlamaIo;

public class Course {
	int _id;
	String _name;
	int _instructorId;
	
	
	public int getId() {
		return _id;
	}
	public void setId(int id) {
		_id = id;
	}
	public String getName() {
		return _name;
	}
	public void setName(String name) {
		_name = name;
	}
	public int getInstructorId() {
		return _instructorId;
	}
	public void setInstructorId(int instructorId) {
		_instructorId = instructorId;
	}
	
	
	
}